//Инициализирует и возвращает модель теплораспределения в объеме
HeatConductivity3DModelTest *InitRoutines::GetInitedHeatConductivity3DModel(size_t dimX, size_t dimY, size_t dimZ) {
	auto t = Sonsode::HostData3D<float>(dimX, dimY, dimZ);

	for (size_t x = 0; x < t.dimX(); x++) {
		for (size_t y = 0; y < t.dimY(); y++) {
			for (size_t z = 0; z < t.dimZ(); z++) {
				if (x == 0)
					t(x, y, z) = 100.0f;
				else if (x == t.dimX() - 1)
					t(x, y, z) = 200.0f;
				else if (y == 0)
					t(x, y, z) = 300.0f;
				else if (y == t.dimY() - 1)
					t(x, y, z) = 400.0f;
				else if (z == 0)
					t(x, y, z) = 500.0f;
				else if (z == t.dimZ() - 1)
					t(x, y, z) = 600.0f;
				else
					t(x, y, z) = 0.0f;
			}
		}
	}

	float h = 0.5f;
	float a = 0.3f;
	float tau = 0.1f;

	auto heatConductivity3DModel = new HeatConductivity3DModelTest(t, h, a, tau);
	heatConductivity3DModel->curPlane = Vector3D<size_t>(t.dimX() / 2, t.dimY() / 2, t.dimZ() / 2);

	return heatConductivity3DModel;
}

